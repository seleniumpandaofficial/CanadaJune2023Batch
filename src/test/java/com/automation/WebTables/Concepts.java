package com.automation.WebTables;

public class Concepts {
	
	//table[@class = 'dataTable'] --> represents the whole WebTable
	//table[@class = 'dataTable']/thead --> represents the WebTable header row
	//table[@class = 'dataTable']/thead/tr --> represents the WebTable header row but more accurate
	//table[@class = 'dataTable']/thead/tr/th --> represents all the cols of the header row
	//table[@class = 'dataTable']/thead/tr/th[1] --> represents the first col of the header row
	//table[@class = 'dataTable']/tbody --> represents the whole webtable but without the header row
	//table[@class = 'dataTable']/tbody/tr --> represents all the rows of the webtable excepting the header row
	//table[@class = 'dataTable']/tbody/tr[1] --> represents the first row of the webtable body
	//table[@class = 'dataTable']/tbody/tr[1]/td --> represents all cols of the 1st row
	//table[@class = 'dataTable']/tbody/tr/td --> represents all the cols of all the rows
	//table[@class = 'dataTable']/tbody/tr[3]/td[5] --> represents 5th col of the 3rd row
	//table[@class = 'dataTable']/tbody/tr/td[1] --> represents all the values in the 1st col
	//table[@class = 'dataTable']/tbody/tr/td[4] --> represents all the values in the 4th col

}
